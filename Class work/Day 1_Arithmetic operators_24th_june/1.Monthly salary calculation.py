#Write a Python program to calculate the total monthly salary of an employee by adding the fixed salary and incentive amount.
#input the fixed salary.
fixed_salary = float(input("Enter the fixed salary :"))
#input the incentive amount.
incentive_amount = float(input("Enter the incentive amount :"))
#calculate the total monthly salary by adding the fixed salary and incentive amount.
total_monthly_salary = fixed_salary + incentive_amount
#print the total monthly salary.
print("Total monthly salary of the employee is :", total_monthly_salary)
